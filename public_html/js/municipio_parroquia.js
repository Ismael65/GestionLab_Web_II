            // Any containing the same child id are added to   Cualquiera que contenga el mismo ID de niño se agrega a
            // the second dropdown box   el segundo cuadro desplegable
            for ( x = 0 ; x < ItemArray.length  ; x++ )
            {
                    if (GroupArray[x] == control.value)
            {
            myEle = document.createElement("option");
            myEle.value = x;
          myEle.text = ItemArray[x] ;
          controlToPopulate.add(myEle) ;
        }
    }
